//Armazenar função em uma variável ou constante

const imprimirSoma = function (a,b){
console.log(a+b)

}

imprimirSoma(2,3)

//Armazenando uma função Arrow em uma variável

const soma = (a,b) => {
return a + b

}

console.log(soma(4,7))

// retorno implícito

const subtracao = (a,b) => a-b 
console.log(subtracao(5,7))

