// operador de atribuição

const a = 7
let b = 4

// atribuição aditiva

b += a // b= b +a 
console.log(b)

// atribuição negativa

b-= a // b = b - a

console.log(b)

// atribuição multiplicativa

b*= 4 // b = b *4 
console.log(b)

// atribuição divisiva

b/= 6 // b = b /6 
console.log(b.toFixed(2))

// atribuição de módulo

b %= 2 // b= b % 2

console.log(b)

