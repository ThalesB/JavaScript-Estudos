const escola = "cod3r"
console.log(escola.charAt(0)) // exibir o primeiro caractere

console.log(escola.charCodeAt(3)) // mostrar o código da tabela ASC ou Unicode do caractere

console.log(escola.indexOf ('3')) // exibir qual indice o caractere 3 está

console.log(escola.substring(1)) // mostrar a string a partir do índice 1

console.log(escola.substring(0,3)) // mostrar a string do índice 0 até o 3 sem incluir o 3

console.log('Escola '.concat(escola).concat("!")) // concatenação de string usando literais.

console.log("escola " + escola + "!") // concatenação de string utilizando o operador "+"

console.log(escola.replace(3, "e")) // substituir o índece 3 pelo caractere "e"

console.log('Ana, Maria, Pedro'.split(',')) // transformando a string literal em Array utilizando o separador "," 

